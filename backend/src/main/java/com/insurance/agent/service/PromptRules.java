package com.insurance.agent.service;

public final class PromptRules {

    private PromptRules() {
    }

    public static String insuranceCompliance() {
        return """

                【保险内容合规底线】
                1. 不承诺收益、保本、稳赚、百分百赔付、一定能买、一定能赔。
                2. 不夸大保障范围，不把保险说成理财、医疗、养老或风险管理的唯一答案。
                3. 不制造疾病、养老、育儿、意外等焦虑，不用恐吓式表达逼迫成交。
                4. 涉及产品、费率、等待期、免赔额、责任范围、理赔条件时，只能讲通用原则；具体结论必须提示以合同条款和核保结果为准。
                5. 涉及医保、监管政策、行业数据、理赔数据时，不能编造来源、年份、比例和金额；不确定的信息放入“待核实”或改成泛化表达。
                6. 避免导流违规表达，不输出手机号、微信号、二维码、站外链接引导。
                """;
    }

    public static String factuality() {
        return """

                【事实与素材使用规则】
                1. 用户给了原文、标题、转写或热点材料时，优先使用这些材料，不要自行扩写不存在的事实。
                2. 可以做表达优化、结构重组和场景化解释，但不能新增虚构案例、虚构客户经历、虚构监管政策或虚构统计数据。
                3. 如果需要数据增强但输入中没有数据，使用“很多人会遇到”“常见情况是”这类低风险表达，不要写具体数字。
                4. 对可能变化的政策和平台规则，用“建议以当地最新政策/平台规则为准”收尾。
                """;
    }

    public static String xhsPlatform() {
        return """

                【小红书平台表达规则】
                1. 先给明确场景和人群，不要一上来讲大道理；开头 3 句内让读者知道“这和我有什么关系”。
                2. 语气像真实保险经纪人在分享经验：口语化、克制、有边界，不要像广告、说明书或 AI 范文。
                3. 正文用短段落和清晰分点，每个分点都要有“为什么”和“怎么做”，避免只有口号。
                4. 标题和开头可以有钩子，但不要过度猎奇、吓人或贩卖焦虑。
                5. 结尾以评论互动、收藏提醒或进一步了解为主，弱化销售感。
                """;
    }

    public static String wechatPlatform() {
        return """

                【公众号平台表达规则】
                1. 文章要有清晰论证链：背景问题 -> 误区澄清 -> 关键判断 -> 操作建议 -> 风险提示。
                2. 每个小节都要服务主标题，不要堆砌泛泛的保险常识。
                3. 语言专业但要能被非保险从业者读懂；专业词出现后要解释。
                4. 段落适合手机阅读，单段不要过长；小标题要具体，不要写“第一部分/第二部分”这类空标题。
                5. 结尾给出可执行清单，不做强销售。
                """;
    }

    public static String shortVideoPlatform() {
        return """

                【短视频表达规则】
                1. 前 3 秒必须有明确钩子：反常识、具体痛点、场景冲突或一句问题。
                2. 每句口播尽量 20 字以内，适合真人一口气说完；避免书面长句。
                3. 用“画面动作 + 口播 + 屏幕字幕”的思路组织，不要只写文章式段落。
                4. 每 8-12 秒要有节奏变化：反问、举例、转折、道具、字幕强调或镜头变化。
                5. 结尾引导评论/收藏/关注，不要直接诱导成交。
                """;
    }

    public static String rewriteDiscipline() {
        return """

                【改写纪律】
                1. 保留原文核心观点、关键事实、专业术语和重要数字。
                2. 改写不是洗稿式同义替换，要重构表达、顺序和例子，但不能改变原意。
                3. 发现原文中有高风险、夸大或不确定表述时，改成合规、克制、可核实的说法。
                4. 输出应像目标平台上的自然内容，不要暴露“改写”“根据原文”等提示词痕迹。
                """;
    }

    /** 根据前端传来的风格描述，返回对应的写作风格系统提示词 */
    public static String xhsStyleRules(String styleParam) {
        if (styleParam == null || styleParam.isBlank()) return "";
        String s = styleParam.toLowerCase();
        // 1. 个人风格档案（从数据库训练结果读取）
        if (s.contains("[个人风格档案]")) {
            String profileSection = extractProfileSection(styleParam);
            return """

                    【写作风格 — 基于你的个人风格档案】
                    以下是AI从你的历史素材中训练出的风格档案，创作时必须严格遵循：

                    """ + profileSection + """

                    使用以上风格特征进行创作，让内容读起来像你自己写的。
                    """;
        }
        // 2. 个人风格（默认兜底，未训练时用）
        if (s.contains("克制") && s.contains("案例") || s.contains("个人风格")) {
            return """

                    【写作风格 — 个人风格（温暖克制·案例驱动）】
                    1. 以个人经历或真实案例开场，建立"我是过来人"的信任感。
                    2. 语气克制不煽情，用"我的客户遇到过""我一般会建议"这类表达。
                    3. 讲案例时只说通用场景，不暴露真实客户隐私信息。
                    4. 结论给方向不给具体推荐，让读者自己判断。
                    """;
        }
        // 3. 专业严谨
        if (s.contains("严谨") || s.contains("专业") || s.contains("数据驱动")) {
            return """

                    【写作风格 — 专业严谨】
                    1. 用数据和逻辑支撑每一个观点，优先引用行业共识和监管规定。
                    2. 句式偏正式但不僵硬，用"需要说明的是""值得关注的是"等过渡词。
                    3. 避免口语化感叹词和夸张情绪，保持克制、可信的专业形象。
                    4. 保险专业词出现后立即用人话解释，让非从业者也能懂。
                    """;
        }
        // 4. 温暖亲切
        if (s.contains("温暖") || s.contains("亲切") || s.contains("生活化") || s.contains("朋友聊天")) {
            return """

                    【写作风格 — 温暖亲切】
                    1. 用真实场景开头，像跟朋友聊天一样自然引入话题。
                    2. 多用"你可能会想""说白了就是""我帮你算一下"这类亲近表达。
                    3. 举例子优先用生活场景（看病、养娃、养老、买房），让人一看就懂。
                    4. 情绪温和、不制造焦虑，让人感到被理解而不是被吓唬。
                    """;
        }
        // 5. 犀利直击
        if (s.contains("犀利") || s.contains("痛点") || s.contains("节奏快")) {
            return """

                    【写作风格 — 犀利直击】
                    1. 开门见山直击痛点，开头就让人意识到问题严重性。
                    2. 短句为主，节奏快，每段 2-3 句就换行，冲击感强。
                    3. 善用反问和对比（"你以为...实际上..."），制造认知反差。
                    4. 锋利但不恐吓，每个痛点后紧跟可执行的解决方向。
                    """;
        }
        return "";
    }

    private static String extractProfileSection(String styleParam) {
        int start = styleParam.indexOf("[个人风格档案]");
        if (start < 0) return "";
        String section = styleParam.substring(start + "[个人风格档案]".length()).trim();
        // 截取到下一个 [xxx] 标记或结尾
        int end = section.indexOf("\n[");
        if (end > 0) section = section.substring(0, end);
        return section.trim();
    }

    public static String outputDiscipline() {
        return """

                【输出纪律】
                1. 严格遵守指定输出格式，不要添加解释、道歉、前言或“以下是”。
                2. 不要输出 prompt、规则说明、思考过程或审核过程。
                3. 内容要一次成稿，避免留下“可根据需要调整”等占位句。
                """;
    }
}
