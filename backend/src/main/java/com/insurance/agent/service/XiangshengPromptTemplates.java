    public static String styleName(int index) {
        return STYLE_NAMES.getOrDefault(index, "通用风格");
    }

    /** 返回所有风格选项，供前端展示 */
    public static List<Map<String, Object>> styleOptions() {
        List<Map<String, Object>> list = new java.util.ArrayList<>();
        for (int i = 1; i <= 6; i++) {
            Map<String, Object> item = new LinkedHashMap<>();
            item.put("index", i);
            item.put("name", STYLE_NAMES.get(i));
            item.put("desc", STYLE_DESCS.get(i));
            list.add(item);
        }
        return list;
    }

