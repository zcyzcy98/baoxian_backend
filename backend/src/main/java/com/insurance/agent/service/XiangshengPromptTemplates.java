    private static String styleBody(int index) {
        return switch (index) {
            case 2 -> beijingStyle();
            case 3 -> shanghaiStyle();
            case 4 -> dongbeiStyle();
            case 5 -> hongkongStyle();
            case 6 -> academicStyle();
            default -> generalStyle();
        };
    }

